# Relational Operators

# > ; < ; >= ; <= ; == ; !=

age = 18

if age <= 15 and age > 0:
    print("You are younger than 16 !")
elif age == 18 or age >= 18:
    print("You are 18 !")
else:
    print("You are older than 16 !")



